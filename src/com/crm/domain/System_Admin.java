package com.crm.domain;

public class System_Admin {
	private String SAid;
	private String SApwd;
	private String SAname;
	private String SAgender;
	private int SAage;
	
	
	public System_Admin() {
		super();
	}

	public System_Admin(String sAid, String sApwd, String sAname,
			String sAgender, int sAage) {
		super();
		SAid = sAid;
		SApwd = sApwd;
		SAname = sAname;
		SAgender = sAgender;
		SAage = sAage;
	}

	public String getSAid() {
		return SAid;
	}

	public String getSApwd() {
		return SApwd;
	}

	public String getSAname() {
		return SAname;
	}

	public String getSAgender() {
		return SAgender;
	}

	public int getSAage() {
		return SAage;
	}

	public void setSAid(String sAid) {
		SAid = sAid;
	}

	public void setSApwd(String sApwd) {
		SApwd = sApwd;
	}

	public void setSAname(String sAname) {
		SAname = sAname;
	}

	public void setSAgender(String sAgender) {
		SAgender = sAgender;
	}

	public void setSAage(int sAage) {
		SAage = sAage;
	}
}
